#include <unistd.h>                                                                                                              
                                                                                                                                 
/**                                                                                                                              
*                                                                                                                               
* _alphabet - Write the char c to stdout                                                                                         
*                                                                                                                               
* Return: Always 0                                                                                                              
*/
void print_alphabet(void);
{
return (write(1, &c, 1));                                                                                                
}
